N = int(input())
for i in range(N) :
    print( '*' * ( i+1 ) + ' ' * ( 2 * ( N - (i+1) ) ) + '*' * (i+1) )

N -= 1 # N = 4

for i in range(N) : 
    print( '*' * ( N-i ) + ' ' * ( 2 * (i+1) ) + '*' * (N-i) )